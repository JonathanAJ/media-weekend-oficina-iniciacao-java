public class Pessoa{
	
	private String nome;
	private String cidade;
	private int idade;

	public void setNome(String nome){
		this.nome = nome;
	}

	public String getNome(){
		return nome;
	}

	public void setCidade(String cidade){
		this.cidade = cidade;
	}

	public String getCidade(){
		return cidade;
	}

	public void setIdade(int idade){
		this.idade = idade;
	}

	public int getIdade(){
		return idade;
	}
}